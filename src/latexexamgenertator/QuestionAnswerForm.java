    private void JbtnSaveGenerateExamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnSaveGenerateExamActionPerformed
        try {
            // TODO add your handling code here:
            LatexFileManager ltx=new LatexFileManager(LatexExamGenertator.ExamFolderPath+""+LatexExamGenertator.ExamFileName);
            //ExamForm ex=new ExamForm(LatexExamGenertator.IsDatabase);
            if(LatexExamGenertator.IsDatabase)
            {
                ExamForm.LoadData();
            }
            ltx.GenerateLatex(); 
            ExamForm.SaveBinary();
            jlblQuestionCounter.setText(ExamForm.AllQuestions.size()+"");
            JOptionPane.showMessageDialog(this, "Models and Answer Key Generated");
        }
        catch (Exception e)
        {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_JbtnSaveGenerateExamActionPerformed

