    public void InsertQuestion()
    {
        try {
            // TODO add your handling code here:
            Question q=new Question();
            q.Desc=jtxtAreaQuestion.getText();
            int ParentId=q.InserQuestion();
            if (ParentId==0)
            {
                JOptionPane.showMessageDialog(null, "Duplicate Question", "Duplication Error", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
            Answer a=new Answer();
            a.Desc=jTextField1.getText();
            a.InsertAnswer(ParentId,jRadioButton1.isSelected());
            a=new Answer();
            a.Desc=jTextField2.getText();
            a.InsertAnswer(ParentId,jRadioButton2.isSelected());
             a=new Answer();
            a.Desc=jTextField3.getText();
            a.InsertAnswer(ParentId,jRadioButton3.isSelected());
            a=new Answer();
            a.Desc=jTextField4.getText();
            a.InsertAnswer(ParentId,jRadioButton4.isSelected());
            a=new Answer();
            a.Desc=jTextField5.getText();
            a.InsertAnswer(ParentId,jRadioButton5.isSelected());
            clearUI();
            }
        }
        catch (Exception e)
        {
            System.out.println(e);
        }
        jlblQuestionCounter.setText(ExamForm.AllQuestions.size()+"");
    }
