    public  void loadQuestionForm(Question x)
    {
        jtxtAreaQuestion.setText(x.Desc);
        jTextField1.setText(x.ArrayListAnswers.get(0).Desc);
        if (x.ArrayListAnswers.get(0).IsCorrect==1)
        {jRadioButton1.setSelected(true);}
        jTextField2.setText(x.ArrayListAnswers.get(1).Desc);
        if (x.ArrayListAnswers.get(1).IsCorrect==1)
        {jRadioButton2.setSelected(true);}
        jTextField3.setText(x.ArrayListAnswers.get(2).Desc);
        if (x.ArrayListAnswers.get(2).IsCorrect==1)
        {jRadioButton3.setSelected(true);}
        jTextField4.setText(x.ArrayListAnswers.get(3).Desc);
        if (x.ArrayListAnswers.get(3).IsCorrect==1)
        {jRadioButton4.setSelected(true);}
        jTextField5.setText(x.ArrayListAnswers.get(4).Desc);
        if (x.ArrayListAnswers.get(4).IsCorrect==1)
        {jRadioButton5.setSelected(true);}
    }
